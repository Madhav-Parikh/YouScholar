<div class="scholarship-details">
<div class="scholarship-item">
<h3>Scholarship Description:</h3><p>Ministry of Social Justice and Empowerment invites applications for&nbsp;National Overseas Scholarship Scheme for SC 2021-22<strong>.&nbsp;</strong>The scholarship aims to support the overseas higher education of&nbsp;low-income students belonging to Scheduled Caste&nbsp;(SC), Denotified Nomadic and Semi-Nomadic Tribe, Landless Agricultural Labourer&nbsp;and Traditional Artisan&nbsp;categories. Financial assistance is offered to students for pursuing a Master's degree or PhD abroad to help improve their economic and social status. The scholarship recipients will receive an annual maintenance allowance of&nbsp;USD 15,400 and other benefits.</p>
</div>
<div class="scholarship-item">
<h3>Eligibility</h3>
<div><p>To be eligible, an applicant must:</p>

<ul>
  <li>Be willing to pursue a master's level course and PhD abroad</li>
  <li>Have obtained at least 60% marks or equivalent grade in relevant Master's&nbsp;degree (for PhD)
  <ul>
  </ul>
  </li>
  <li>Have obtained at least 60% marks or equivalent grade in relevant Bachelor's&nbsp;degree (for Master's degree)</li>
  <li>Be below 35 years of age, as on 1st April 2021</li>
  <li>Have an annual family income not exceeding INR 8 lakh per annum</li>
  <li>Belong to any of the following categories:
  <ul>
    <li>Scheduled Castes (SC)</li>
    <li>Denotified Nomadic Tribes</li>
    <li>Semi-Nomadic Tribes</li>
    <li>Landless Agricultural Labourers</li>
    <li>Traditional Artisans</li>
  </ul>
  </li>
</ul>

<p><strong>Note</strong>: Not more than two children of the same parents/guardians will be eligible for a scholarship under this scheme.&nbsp;</p></div>
</div>
<div class="scholarship-item">
<h3>Benefits</h3>
<div><p>The selected applicants will receive the following benefits:</p>

<p><strong>For studies in the USA and other countries except for UK- </strong></p>

<ul>
  <li>The annual maintenance allowance of USD 15,400&nbsp;</li>
  <li>Contingency allowance of USD 1,500</li>
  <li>Tuition fees (actual as charged)</li>
  <li>Visa fees (actual visa fees)</li>
  <li>Medical Insurance Premium</li>
  <li>Incidental Journey Allowance and Equipment Allowance</li>
  <li>Poll Tax</li>
  <li>Air passage</li>
</ul>

<p><strong>For studies in the UK- </strong></p>

<ul>
  <li>The annual maintenance allowance of GBP 9,900</li>
  <li>Contingency allowance of GBP 1,100</li>
  <li>Tuition fees (actual as charged)</li>
  <li>Visa fees (actual visa fees)</li>
  <li>Medical Insurance Premium</li>
  <li>Incidental Journey Allowance and Equipment Allowance</li>
  <li>Poll Tax</li>
  <li>Air passage</li>
</ul></div>
</div>
<div class="scholarship-item">
<h3>Documents</h3>
<div><ul>
  <li>Caste/Category&nbsp;certificate</li>
  <li>Date of birth certificate</li>
  <li>Photo</li>
  <li>Scanned signature</li>
  <li>Current address proof</li>
  <li>Permanent address proof</li>
  <li>Qualifying mark sheet/Degree</li>
  <li>Valid document for marks</li>
  <li>Valid document regarding admission in Foreign University&nbsp;</li>
  <li>Income certificate of all family members contributing to the household</li>
  <li>Employer's NOC certificate (if the applicant is employed)</li>
</ul></div>
</div>
<div class="scholarship-item">
<h3>How can you apply?</h3>
<div><div><p>Follow the steps to apply for the scholarship:</p>

<p><strong>Step 1:&nbsp;</strong>Click on the "<strong>Apply Now"</strong>&nbsp;button below&nbsp;and register.<br>
<strong>Step 2:</strong>&nbsp;Log in with the username and password to start the application.<br>
<strong>Step 3:</strong> Provide personal details, address, foreign university details, etc.<br>
<strong>Step 4:</strong> Provide educational and employment details, visa details, etc<br>
<strong>Step 5:</strong> Submit the form online.</p></div>
