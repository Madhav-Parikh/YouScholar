<div class="scholarship-details">
<div class="scholarship-item">
<h3>Scholarship Description:</h3>
<p><strong>Keep India Smiling Foundational Scholarship Programme</strong> aims to provide foundational support to individuals, who are deserving &amp; meritorious but may lack resources to pursue their dreams. Along with the financial support, programme will also focus on mentorship and career guidance to the beneficiaries as and when required.</p>
</div>
<div class="scholarship-item">
<h3>Eligibility</h3>
<div><ul>
  <li>Must have passed Class 12 board examination in 2020</li>
  <li>Must have scored a minimum 60% in Class 12 board exams</li>
  <li>Must be enrolled in any recognised Bachelor of Dental Surgery&nbsp;programme in India</li>
  <li>Annual family income should be less than INR 5 Lakh</li>
</ul></div>
</div>
<div class="scholarship-item">
<h3>Benefits</h3>
<p>INR 30,000 per annum for 4 years</p></div>
<div class="scholarship-item">
<h3>Documents</h3>
<div><ul>
  <li>Passport-sized photograph</li>
  <li>Valid ID proof – Either of Aadhaar Card/Driving License/Voter Id Card/Pan Card</li>
  <li>Income Proof – Income certificate/BPL certificate/Food security certificate/Any other certificate of income issued by competent government authority.</li>
  <li>Class 12 marksheet</li>
  <li>Fee Receipt/Admission Letter/College ID card/Bonafide certificate (in case admission for professional course already taken)</li>
  <li>Entrance examination result (In case seeking to take admission)</li>
  <li>Disability certificate, in case of any physical disability</li>
</ul></div>
</div>
<div class="scholarship-item">
<h3>How can you apply?</h3>
<div><p>Click on the 'View scholarship' button below.</p></div></div><!--
